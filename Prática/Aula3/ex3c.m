    Funcao de probabilidade: 
    k = numero de ocorrencias a testar.
    lambda = número esperado de ocorrências.
    sendo a probabilidade 0.5 (1 em cada 2 vezes) e 4 lancamentos, 
    lambda = 4/2 = 2

    f(k, lambda) = (e^-2 * 2^k) / k!
